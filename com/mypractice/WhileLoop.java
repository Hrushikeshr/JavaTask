public class WhileLoop {
    public static void main(String[] args) {
        int num = 1;
//        Using While Loop
        while (num <=10){
            System.out.println(num);
            num +=1;
        }
//       Using Do While Loop
        num =1;
        do {
            System.out.println(num);
            num +=1;
        }while (num<=10);
    }
}
